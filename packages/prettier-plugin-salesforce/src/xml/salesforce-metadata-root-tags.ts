export function extractRootTagLocalName(source: string): string | null {
  const match = source.match(/^\s*(?:<\?xml[\s\S]*?\?>\s*)?(?:<!--[\s\S]*?-->\s*)*<([A-Za-z_][A-Za-z0-9_.:-]*)\b/);
  if (!match) {
    return null;
  }
  const tagName = match[1];
  const localName = tagName.includes(":") ? (tagName.split(":").pop() ?? tagName) : tagName;
  return localName.length > 0 ? localName : null;
}
